public class typeCasting {
    public static void main(String[] args) {
        //Type Casting Example
        double x = 15.95674;  
        int douToInt = (int)x;
        System.out.println(douToInt);
        int y = 76;
        double intToDou = (double)y;
        System.out.println(intToDou);
        
    }
}
